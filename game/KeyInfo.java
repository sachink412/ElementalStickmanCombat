package game;

public class KeyInfo {
    // Pressed state of the WASD keys (pressed = true)
    public boolean up, left, right, down;

    // Toggles stickman orientation (right = true) or (left = false)
    public boolean leftRight = true;
    
    // Pressed state of the Q, 1, 2, and 3 keys (pressed = true)
    public boolean q, one, two, three;
}